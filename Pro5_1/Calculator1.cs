using System;
using System.Collections.Generic;
using System.Text;

namespace Pro5_1
{
    /// <summary>
    /// 可进行两个数的四则运算
    /// </summary>
    public class Calculator1
    {
        public float Add(float a, float b)
        {
            return a + b;
        }

        public float Sub(float a, float b)
        {
            return a - b;
        }

        public float Mul(float a, float b)
        {
            return a * b;
        }

        public float Div(float a, float b)
        {
            return a / b;
        }
    }
}
